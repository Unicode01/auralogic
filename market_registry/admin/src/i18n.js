function normalizeLocale(value) {
  const normalized = String(value || '').trim().toLowerCase();
  if (normalized.startsWith('zh')) {
    return 'zh';
  }
  return 'en';
}

function interpolate(message, params) {
  if (!params || typeof params !== 'object') {
    return message;
  }
  return String(message).replace(/\{(\w+)\}/g, (_, key) => {
    if (params[key] === undefined || params[key] === null) {
      return '';
    }
    return String(params[key]);
  });
}

export function getStoredLocale() {
  try {
    const value = localStorage.getItem(LOCALE_STORAGE_KEY);
    if (value) {
      return normalizeLocale(value);
    }
  } catch (error) {
  }
  return 'en';
}

export function translate(locale, key, params) {
  const normalizedLocale = normalizeLocale(locale);
  const rawKey = String(key ?? '');
  const message = normalizedLocale === 'en' ? (EN_MESSAGES[rawKey] || rawKey) : rawKey;
  return interpolate(message, params);
}

const I18nContext = React.createContext({
  locale: 'en',
  setLocale: () => {},
  t: (key, params) => translate('en', key, params),
});

export function I18nProvider({ children }) {
  const [locale, setLocaleState] = React.useState(() => getStoredLocale());

  const setLocale = React.useCallback((nextLocale) => {
    const normalized = normalizeLocale(nextLocale);
    setLocaleState(normalized);
    try {
      localStorage.setItem(LOCALE_STORAGE_KEY, normalized);
    } catch (error) {
    }
  }, []);

  React.useEffect(() => {
    if (typeof document === 'undefined') {
      return;
    }
    document.documentElement.lang = locale === 'zh' ? 'zh-CN' : 'en';
  }, [locale]);

  const value = React.useMemo(() => ({
    locale,
    setLocale,
    t: (key, params) => translate(locale, key, params),
  }), [locale, setLocale]);

  return (
    <I18nContext.Provider value={value}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return React.useContext(I18nContext);
}
