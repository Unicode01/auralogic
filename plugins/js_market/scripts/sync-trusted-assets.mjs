import { promises as fs } from "node:fs";
import { createHash } from "node:crypto";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const pluginRoot = path.resolve(scriptDir, "..");
const assetsDir = path.join(pluginRoot, "assets");
const outputPath = path.join(
  pluginRoot,
  "src",
  "lib",
  "trusted-workspace-assets-fallback.generated.ts"
);

const css = await fs.readFile(path.join(assetsDir, "trusted-workspace.css"), "utf8");
const js = await fs.readFile(path.join(assetsDir, "trusted-workspace.js"), "utf8");
const assetVersion = createHash("sha1").update(css).update("\n--asset-split--\n").update(js).digest("hex").slice(0, 12);

const output = `${[
  "// Auto-generated by scripts/sync-trusted-assets.mjs. Do not edit directly.",
  `export const TRUSTED_WORKSPACE_ASSET_VERSION = ${JSON.stringify(assetVersion)};`,
  `export const TRUSTED_WORKSPACE_CSS_FALLBACK = ${JSON.stringify(css)};`,
  `export const TRUSTED_WORKSPACE_JS_FALLBACK = ${JSON.stringify(js)};`,
  "",
].join("\n")}`;

await fs.writeFile(outputPath, output, "utf8");
